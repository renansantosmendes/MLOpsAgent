class MLOpsTrainingAgent:
    """Production-grade MLOps agent for automated model retraining and deployment.

    Orchestrates the complete model lifecycle: data ingestion, drift detection,
    candidate training, model selection, evaluation against the current
    production model, and conditional deployment via GitHub Actions.

    The internal reasoning loop is implemented as a LangGraph ``StateGraph``
    with conditional edges that mirror the decision rules in the specification.

    Args:
        reference_dataset_path: Filesystem path to the reference dataset CSV
            used as the drift baseline and training anchor.
        target_column: Name of the column to predict in the dataset.
        current_model_metrics_path: Path to the current production model's
            metrics JSON file. Pass ``None`` when no production model exists.
        model_artifact_path: Destination path for the saved model artifact.
        metrics_artifact_path: Destination path for the saved metrics JSON.
        github_owner: GitHub username or organization that owns the repository.
        github_repo: Repository name where the Actions workflow lives.
        workflow_id: Filename or numeric ID of the GitHub Actions workflow
            (e.g. ``"retrain.yml"``).
        target_branch: Git branch ref to pass in the workflow dispatch payload.
        github_token: Personal access token with ``repo`` and
            ``actions:write`` scopes.

    Example:
        >>> agent = MLOpsTrainingAgent(
        ...     reference_dataset_path="data/reference.csv",
        ...     target_column="label",
        ...     current_model_metrics_path="artifacts/metrics.json",
        ...     model_artifact_path="artifacts/model.joblib",
        ...     metrics_artifact_path="artifacts/metrics.json",
        ...     github_owner="acme",
        ...     github_repo="ml-service",
        ...     workflow_id="retrain.yml",
        ...     target_branch="main",
        ...     github_token="ghp_...",
        ... )
        >>> final_state = agent.run("https://example.com/new_data.csv")
    """

    def __init__(
        self,
        reference_dataset_path: str,
        target_column: str,
        current_model_metrics_path: str | None,
        model_artifact_path: str,
        metrics_artifact_path: str,
        github_owner: str,
        github_repo: str,
        workflow_id: str,
        target_branch: str,
        github_token: str,
    ) -> None:
        self._config: dict[str, Any] = {
            "reference_dataset_path": reference_dataset_path,
            "target_column": target_column,
            "current_model_metrics_path": current_model_metrics_path,
            "model_artifact_path": model_artifact_path,
            "metrics_artifact_path": metrics_artifact_path,
            "github_owner": github_owner,
            "github_repo": github_repo,
            "workflow_id": workflow_id,
            "target_branch": target_branch,
            "github_token": github_token,
        }
        self._graph = self._build_graph()

    def _build_graph(self) -> Any:
        """Construct and compile the LangGraph StateGraph.

        Returns:
            A compiled LangGraph runnable graph.
        """
        workflow = StateGraph(AgentState)

        workflow.add_node("data_process", node_data_process)
        workflow.add_node("data_drift", node_data_drift)
        workflow.add_node("model_training", node_model_training)
        workflow.add_node("model_selection", node_model_selection)
        workflow.add_node("model_evaluation", node_model_evaluation)
        workflow.add_node("deploy", node_deploy)
        workflow.add_node("halt_no_change", node_halt_no_change)
        workflow.add_node("halt_underperforms", node_halt_underperforms)

        workflow.set_entry_point("data_process")

        workflow.add_conditional_edges(
            "data_process",
            route_after_data_process,
            {
                "data_drift": "data_drift",
                "halt_no_change": "halt_no_change",
            },
        )
        workflow.add_conditional_edges(
            "data_drift",
            route_after_data_drift,
            {
                "model_training": "model_training",
                "halt_no_change": "halt_no_change",
            },
        )

        workflow.add_edge("model_training", "model_selection")
        workflow.add_edge("model_selection", "model_evaluation")

        workflow.add_conditional_edges(
            "model_evaluation",
            route_after_evaluation,
            {
                "deploy": "deploy",
                "halt_underperforms": "halt_underperforms",
            },
        )

        workflow.add_edge("deploy", END)
        workflow.add_edge("halt_no_change", END)
        workflow.add_edge("halt_underperforms", END)

        return workflow.compile()

    def run(self, input_source: str | pd.DataFrame) -> AgentState:
        """Execute the full MLOps pipeline for the given input data.

        Args:
            input_source: Either a URL pointing to a CSV file or a pre-loaded
                pandas DataFrame containing the new data to process.

        Returns:
            The final ``AgentState`` after the graph has reached a terminal
            node. Inspect ``state["halt_reason"]`` for early-exit details or
            ``state["deployment_triggered"]`` to confirm a successful deploy.

        Raises:
            Any exception propagated from the underlying tool functions.
        """
        initial_state: AgentState = {**self._config, "input_source": input_source}
        logger.info(
            "MLOpsTrainingAgent.run: starting pipeline for input_source=%s",
            input_source if isinstance(input_source, str) else f"<DataFrame shape={input_source.shape}>",
        )
        final_state: AgentState = self._graph.invoke(initial_state)
        return final_state

