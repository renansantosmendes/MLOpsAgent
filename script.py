from typing import TypedDict


class AgentState(TypedDict, total=False):
    """Carries all inter-step state through the LangGraph StateGraph.

    Attributes:
        input_source: Incoming data source — URL string or DataFrame.
        reference_dataset_path: Path to the on-disk reference dataset CSV.
        target_column: Name of the column to predict.
        current_model_metrics_path: Path to the production model metrics JSON.
        model_artifact_path: Destination path for the saved model joblib file.
        metrics_artifact_path: Destination path for the saved metrics JSON.
        github_owner: GitHub repo owner for the Actions dispatch.
        github_repo: GitHub repo name for the Actions dispatch.
        workflow_id: GitHub Actions workflow filename or ID.
        target_branch: Git branch for the workflow dispatch.
        github_token: GitHub personal access token.
        reference_data: Reference DataFrame after data_process_tool.
        new_data: New-records-only DataFrame after data_process_tool.
        merged_data: Merged (reference + new) DataFrame.
        has_new_records: Whether any new records were found.
        drift_detected: Whether Evidently detected significant drift.
        drift_report: Per-feature drift summary dict.
        drift_share: Share of drifted features.
        candidate_results: Raw CV results from model_training_tool.
        feature_columns: Ordered list of feature column names.
        label_encoder: Fitted LabelEncoder for the target.
        train_val_data: 85% split used for training.
        test_data: Held-out 15% test split.
        best_estimator: The selected best fitted Pipeline.
        selected_model_name: Name of the winning candidate model.
        all_candidates_metrics: Summary metrics for all evaluated candidates.
        new_model_metrics: Test-set metrics for the new model.
        current_model_metrics: Production model metrics loaded from disk.
        is_better: Whether the new model clears the improvement threshold.
        deployment_triggered: Whether the GitHub workflow was dispatched.
        http_status_code: HTTP status code from the GitHub API.
        halt_reason: Human-readable reason when the agent halts early.
    """

    input_source: str | pd.DataFrame
    reference_dataset_path: str
    target_column: str
    current_model_metrics_path: str | None
    model_artifact_path: str
    metrics_artifact_path: str
    github_owner: str
    github_repo: str
    workflow_id: str
    target_branch: str
    github_token: str

    reference_data: pd.DataFrame
    new_data: pd.DataFrame
    merged_data: pd.DataFrame
    has_new_records: bool

    drift_detected: bool
    drift_report: dict
    drift_share: float

    candidate_results: dict
    feature_columns: list
    label_encoder: LabelEncoder
    train_val_data: pd.DataFrame
    test_data: pd.DataFrame

    best_estimator: Pipeline
    selected_model_name: str
    all_candidates_metrics: dict

    new_model_metrics: dict
    current_model_metrics: dict | None
    is_better: bool

    deployment_triggered: bool
    http_status_code: int
    halt_reason: str


