from langgraph.graph import StateGraph, END


def node_data_process(state: AgentState) -> AgentState:
    """LangGraph node: run data ingestion and comparison.

    Args:
        state: The current agent state carrying ``input_source`` and
            ``reference_dataset_path``.

    Returns:
        Updated state with ``reference_data``, ``new_data``, ``merged_data``,
        and ``has_new_records`` populated.
    """
    result = data_process_tool(
        input_source=state["input_source"],
        reference_dataset_path=state["reference_dataset_path"],
    )
    return {**state, **result}


def node_data_drift(state: AgentState) -> AgentState:
    """LangGraph node: run Evidently drift detection.

    Args:
        state: The current agent state with ``reference_data`` and ``new_data``.

    Returns:
        Updated state with ``drift_detected``, ``drift_report``, and
        ``drift_share`` populated.
    """
    result = data_drift_tool(
        reference_data=state["reference_data"],
        new_data=state["new_data"],
    )
    return {**state, **result}


def node_model_training(state: AgentState) -> AgentState:
    """LangGraph node: train candidate models via cross-validation.

    Args:
        state: The current agent state with ``merged_data`` and
            ``target_column``.

    Returns:
        Updated state with ``candidate_results``, ``feature_columns``,
        ``label_encoder``, ``train_val_data``, and ``test_data`` populated.
    """
    result = model_training_tool(
        input_data_train=state["merged_data"],
        target_column=state["target_column"],
    )
    return {**state, **result}


def node_model_selection(state: AgentState) -> AgentState:
    """LangGraph node: select the best candidate model.

    Args:
        state: The current agent state with ``candidate_results``.

    Returns:
        Updated state with ``best_estimator``, ``selected_model_name``, and
        ``all_candidates_metrics`` populated.
    """
    result = model_selection_tool(candidate_results=state["candidate_results"])
    return {**state, **result}


def node_model_evaluation(state: AgentState) -> AgentState:
    """LangGraph node: evaluate the best model on the test split.

    Args:
        state: The current agent state with ``best_estimator``, ``test_data``,
            ``target_column``, and ``current_model_metrics_path``.

    Returns:
        Updated state with ``new_model_metrics``, ``current_model_metrics``,
        and ``is_better`` populated.
    """
    test_features = state["test_data"].drop(columns=[state["target_column"]])
    test_target = state["test_data"][state["target_column"]]
    result = model_evaluation_tool(
        trained_pipeline=state["best_estimator"],
        input_data_test=test_features,
        target_test=test_target,
        current_model_metrics_path=state.get("current_model_metrics_path"),
    )
    return {**state, **result}


def node_deploy(state: AgentState) -> AgentState:
    """LangGraph node: persist the model artifact and trigger GitHub Actions.

    Args:
        state: The current agent state with all deployment configuration and
            the fitted ``best_estimator``.

    Returns:
        Updated state with ``deployment_triggered`` and ``http_status_code``
        populated.
    """
    result = deploy_application_tool(
        trained_pipeline=state["best_estimator"],
        new_model_metrics=state["new_model_metrics"],
        model_artifact_path=state["model_artifact_path"],
        metrics_artifact_path=state["metrics_artifact_path"],
        github_owner=state["github_owner"],
        github_repo=state["github_repo"],
        workflow_id=state["workflow_id"],
        target_branch=state["target_branch"],
        github_token=state["github_token"],
    )
    logger.info("Deployment triggered successfully.")
    return {**state, **result}


def node_halt_no_change(state: AgentState) -> AgentState:
    """LangGraph node: halt because no drift and no new records were detected.

    Args:
        state: The current agent state.

    Returns:
        Updated state with ``halt_reason`` set.
    """
    reason = "No drift detected, no new data. Skipping retraining."
    logger.warning(reason)
    return {**state, "halt_reason": reason}


def node_halt_underperforms(state: AgentState) -> AgentState:
    """LangGraph node: halt because the new model does not beat production.

    Args:
        state: The current agent state with ``new_model_metrics`` and
            ``current_model_metrics``.

    Returns:
        Updated state with ``halt_reason`` set.
    """
    new_f1 = state.get("new_model_metrics", {}).get("f1_macro", float("nan"))
    prod_f1 = (
        state.get("current_model_metrics") or {}
    ).get("f1_macro", float("nan"))
    reason = (
        f"New model underperforms. Skipping deployment. "
        f"new_f1_macro={new_f1:.4f}, production_f1_macro={prod_f1:.4f}"
    )
    logger.warning(reason)
    return {**state, "halt_reason": reason}


# ---------------------------------------------------------------------------
# Conditional edge routing functions
# ---------------------------------------------------------------------------


def route_after_data_process(state: AgentState) -> str:
    """Route after data_process_tool: drift check or immediate halt.

    Args:
        state: Current agent state with ``has_new_records``.

    Returns:
        Node name to transition to next.
    """
    if state.get("has_new_records"):
        return "data_drift"
    return "halt_no_change"


def route_after_data_drift(state: AgentState) -> str:
    """Route after data_drift_tool: train or halt.

    Args:
        state: Current agent state with ``drift_detected`` and
            ``has_new_records``.

    Returns:
        Node name to transition to next.
    """
    if state.get("drift_detected") or state.get("has_new_records"):
        return "model_training"
    return "halt_no_change"


def route_after_evaluation(state: AgentState) -> str:
    """Route after model_evaluation_tool: deploy or halt.

    Args:
        state: Current agent state with ``is_better``.

    Returns:
        Node name to transition to next.
    """
    if state.get("is_better"):
        return "deploy"
    return "halt_underperforms"


